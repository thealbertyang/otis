		            </div>
		            <div class="col s12 m12 l6">
		              	<p>When it comes to visuals, keep it simple. You want the viewers to be able to read the screen, even if they’re far back. That means you need large font and a color that will stand out against the background you choose. The goal is to have the audience listen to you – not read your slides. Less is more.</p>

						<p>Any images or backdrops you choose should add to the presentation and not distract or confuse people. Color choice is also important, because you may need to remain sensitive to a client’s brand or your own company colors. As always, text must be readable. Loud and obnoxious colors don’t automatically mean they’ll grab peoples’ attention. Often it can be so distracting that you do just the opposite.
