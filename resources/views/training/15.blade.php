		            <div class="col s12 m12 l6">
		              	<p>When you feel ready to pull together the actual presentation, keep brevity in mind. It doesn’t matter whether you’re using visuals or not, since the same is true for either type of briefing. If you’re making a PowerPoint or other visual, get the text down first. Think in terms of a headline with three to four points each. Any more than that and you’re likely to lose your audience’s attention span.</p>

						<p>Think about whether you want it to be an interactive presentation or strictly lecture style. Asking questions and moving around the room are great ways to keep people on their toes and interested in what you’re saying. If it’s a smaller audience of people who don’t know each other, consider starting with an icebreaker exercise or brief introductions to increase the comfort level in the room.
